# Crie um script Python que leia dois numeros a tente 
# mostrar a soma entre eles.
numero_1 = float(input('Digite o primeiro algarismo\n'))
numero_2 = float(input('Digite o segundo algarismo\n'))
soma = numero_1 + numero_2
print('Soma dos dois algarismos: {} e {}: '.format(numero_1, numero_2), soma)